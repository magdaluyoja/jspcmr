				<h3 id="greeting">日本小児心臓MR研究会 設立にあたって</h3>
				<p><img src="/assets/web/images/banners/sample.jpg" alt="" width="231" height="143" class="alignleft">
					近年、小児循環器領域の診療は大きく発展してきましたが、その中でも心臓超音波検査や多列CTなど画像診断の進歩がその発展に大きく寄与しているのは紛れも無い事実です。<br><br>
					これら著しい進歩のみられる心臓イメージングの中でも、心臓MRは単に形態評価のみならず血行動態や心筋性状評価など多くの機能を発揮し、非侵襲的で大変有用なモダリテイーです。しかし、操作性がやや煩雑なこともあり、わが国では十分に普及しているとは言えないのが現状です。<br><br>
					　そこで2016年７月、小児成育医学領域における心血管MRIに関する臨床応用・研究の発展、会員相互の交流などを目的に、数名の有志を中心に研究会を設立するに至り、研究会名を日本小児心臓MR研究会JSPCMR（Japanese Society of Pediatric Cardiac Magnetic Resonance）と致しました。<br>
					<br>　2017年2月には記念すべき第１回JSPCMR研究会学術集会（石川友一会長）を、2018年３月に第２回学術集会（稲毛章郎会長）を開催し、ともに予想を上回る演題応募および参加者があり、皆さんの心臓MRに対する関心の高さ・ニーズの高さを改めて認識した次第です。<br><br>
					　今後、本研究会において、小児循環器医だけでなく循環器内科医や検査技師などコメデイカルも含め多職種の方々の参加により活発な活動が継続され、心臓MRの普及・発展、ひいては心疾患患者の予後やQOLの更なる改善に寄与することを期待しています。皆様のご協力とご支援を賜りますようお願い申し上げます。<br><br>

